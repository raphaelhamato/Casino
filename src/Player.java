public class Player {
    private String namn;
    private double saldo;

    public Player(){

    }

    public String getNamn() {
        return namn;
    }

    public double getSaldo() {
        return saldo;
    }

    public void setNamn(String namn) {
        this.namn = namn;
    }

    public void setSaldo(double saldo) {
        this.saldo = saldo;
    }
}